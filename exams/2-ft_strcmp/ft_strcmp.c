int		ft_strcmp(char *s1, char *s2)
{
	int		i;
	unsigned char	*cs1;
	unsigned char	*cs2;

	i = 0;
	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	while (cs1[i] && cs2[i] && (cs1[i] == cs2[i]))
		i++;
	return (cs1[i] - cs2[i]);
}
